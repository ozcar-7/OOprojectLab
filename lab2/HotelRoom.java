public class HotelRoom 
{
    private int roomNumber;
    private String roomType;

    public HotelRoom()
        {
        }
    
    public void setRoomNumber(int number)
        {
            roomNumber = number;
        }

    public void setRoomType(String type)
        {
            roomType = type;
        }
    
    public int getRoomNumber()
        {
            return roomNumber;
        }

    public String getRoomType()
        {
            return roomType;
        }


}
